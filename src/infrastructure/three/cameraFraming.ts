/**
 * Чисте обчислення (без three.js/DOM) ефективного вертикального FOV камери
 * для вузьких (портретних) екранів.
 *
 * `PerspectiveCamera.fov` у three.js — це ВЕРТИКАЛЬНИЙ кут огляду; він не
 * залежить від aspect, тож підлога/стеля лишаються в кадрі на будь-якому
 * екрані. Але горизонтальний кут звужується пропорційно aspect — на
 * портретному телефоні (aspect ≈ 0.46) він у ~3.8 рази вужчий, ніж на
 * десктопі (16:9), і найближчі меблі «застигають» на весь кадр.
 *
 * Компенсуємо помірним розширенням vFOV (як ширококутний об'єктив в
 * інтер'єрній фотографії) — це не зсуває камеру в просторі (на відміну від
 * «відкату» дистанції, що на тісних кадрах біля кута кімнати виштовхує
 * камеру крізь стіну) і додатково показує ще більше стелі/підлоги. MAX_FOV —
 * стеля, щоб не отримати надмірний fisheye-викривлення.
 */
const REFERENCE_ASPECT = 16 / 9;
const MAX_FOV = 78;

export function framedFov(baseFov: number, aspect: number): number {
  if (!(aspect > 0) || aspect >= REFERENCE_ASPECT) return baseFov;
  return Math.min(baseFov * (REFERENCE_ASPECT / aspect), MAX_FOV);
}
